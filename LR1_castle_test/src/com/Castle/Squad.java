package com.Castle;


public class Squad {
    private int squadCount;
    private String squadName;

    public Squad(int squadCount, String squadName){
        this.squadCount = squadCount;
        this.squadName = squadName;
    }

    public int getSquadCount() {
        return squadCount;
    }

    public String getsquadName() {
        return squadName;
    }
}
